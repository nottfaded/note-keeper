using MongoDB.Bson.Serialization.Attributes;
using MongoDB.Bson;

namespace DatabaseCore.Models;

public class User
{
    [BsonId]
    [BsonRepresentation(BsonType.ObjectId)]
    public string Id { get; set; } = string.Empty;

    [BsonElement("email")]
    public string Email { get; set; } = string.Empty;

}